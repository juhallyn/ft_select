#include "./libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;
	size_t x;

	i = 0;
	x = 0;
	j = 0;
	i = ft_strlen(s1);
	while (s2[j] && x < n)
	{
		s1[i] = s2[j];
		x++;
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
